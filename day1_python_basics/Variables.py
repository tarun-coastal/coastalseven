# String (text)
name = "Tarun"
city = "Vijayawada"
message = "Hello World"

print(name)
print(city)
print(message)

# Integer (whole numbers)
age = 21
marks = 95
year = 2026

print(age)
print(marks)
print(year)

# Float (decimal numbers)
price = 99.99
height = 5.8
temperature = 36.5

print(price)
print(height)
print(temperature)

# Boolean (True or False)
is_student = True
is_logged_in = False
has_passed = True

print(is_student)
print(is_logged_in)
print(has_passed)