# String (text)
string = "tarun"
print("String value:", string)

# Integer (whole number)
integer = 1
print("Integer value:", integer)

# Float (decimal number)
float = 2.99
print("Float value:", float)

# Boolean (True or False)
boolean = True
print("Boolean value:", boolean)

# List (multiple values, mutable)
list = ["abc", 1]
print("List value:", list)

# Tuple (multiple values, immutable)
tuple = (1, "ads")
print("Tuple value:", tuple)


# set (multiple values ,but repetition not allowed)
set={"a","abfb"}
print(set)

# dictionary(key and value pairs..like word and definiton )
dict={
    "name":"tarun",
    "age":23,
    "village":"guntur"

}
print(dict)